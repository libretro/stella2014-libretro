#include "CartF8SC.hxx"

// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
CartridgeF8SC::CartridgeF8SC(const uint8_t* image, uint32_t size,
                             const Settings& settings)
  : CartridgeF8(image, size, "", settings)
{
  // The only difference from plain F8 is 128 bytes of superchip RAM.
  // The write port ($1000-$107F) and read port ($1080-$10FF) are installed
  // by the base class from myRamSize / myRamWpHigh (write port low = default).
  myRamSize = 128;
}

// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
CartridgeF8SC::~CartridgeF8SC()
{
}
